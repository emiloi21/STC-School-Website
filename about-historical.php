                <div class="row">
                
                  <div class="col-md-8">
                  <p class="lead" style="margin-bottom: 12px;">The STA years: Humble Beginnings</p>
                          <p>
                          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                          Way back in 1940, the response of the urgent need of the "breathe there the men with souls so dead" was realized by Msgr. Alfredo Verzosa, the bishop of Lipa with Mother General's permission, turned over to Mother Mary of the Holy Name Philips, St. Bridget's Superior, the management of the newly organized  school, St. Theresa's Academy (STA) and run by the Franciscan Sisters. Although, records show that the academy was formerly Colegio de Sta. Teresa de Jesus and was established in the 1920's with the core values CONCERN, RESPONSIBILITY, SERVICE.
                          </p>
                          
                          <p>
                          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                          This pre - war STA had complete elementary and high school departments. Sr. Mary of St. Claire governed the high school department while the elementary was handled by Sr. Mary of St. Dorothy. Sr. Mary of the Immaculate Conception came later to help the two sisters.  They were welcomed by then Parish Priest of Bauan, Msgr. Castillo. The school was closed at its infancy at the start of the World War II.
                          </p>
                  </div>
                  <div class="col-md-4"><img alt="" src="img/stc_logo.png" class="img-fluid rounded-circle" style="margin-top: 36px;" /></div>
                  
                  <div class="col-md-12">
                          <p>
                          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                          Bauan, a bustling town, showed a marked progress in a seemingly safe and peaceful atmosphere when  STA was reopened in 1944. That was then the Japanese Imperial Army permitted Mother Clare to reopen the school on the condition that Niponggo would be taught as a subject in all classes. The classes were complete except for the top most years (III and IV) in the high school. The rumblings and tumult of the war of the Philippine Liberation forced STA to close before the school year was over.
                          </p>
                          
                          <p>
                          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                          At the request of the incumbent parish priest, Fr. Gualberto La Torre, STA was started again in 1950 by Sr. Mary of the Holy Name. Classes were not held in the historic seminary which housed the St. Francis de Sales Major seminarians but in the ancient  rectory which was but a glorified  nipa hut. It had a  roofing of nipa shingles, walls of a "new" kind of wood called masonite, had barely two rooms enough to hold classes and a smaller room, multi- purpose room served as office, library, reception room and school store. There were 50 students at the start of the Kindergarten class in the ground floors of the Fathers' new semi - concrete rectory. A wooden partition separated the Kindergarten class from the Grade II class. The detachable partitions were used as framework for stage programs.
                          </p>
                          
                          <p>
                          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                          Nineteen hundred and fifty - one marked the presentation of diplomas to the first Grade VI  graduates; by 1954, to the first high school graduate.  Such were the inauspicious beginnings of the post war  St. Theresa's Academy .  The burgeoning school population could no longer accommodated in the ground floor of the hut so a new school building was built in 1951 on the site of the old hut. The facade was lonely on paper but the reality was below the expectations of Mother  Mary of St. Dominican who followed the minutest details in the planning, construction and painting. The school continued to grow. Sr. Mary of St. Theresa took over by Sr. Ofelia Endrinal and later by Sr. Mary Francis Xavier. The last Good Shepherd sister who administered St. Theresa's academy was Sr. Mary Ann Terrenal. 
                          </p>
                  
                  </div>
                  
                  
                  <div class="col-md-8">
                  <p class="lead" style="margin-top: 36px; margin-bottom: 12px; ">From the 70's to the 90's</p>
                  
                          <p>
                          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                          In 1970, the Good Shepherd Sisters turned over the school to the Archdiocese of Lipa with the Archbishop as the Chairman of the Board of Trustees. The year also marked its expansion in the admission of not only female but also male pupils. The first school director was the Rev. Msgr. Alberto M. Boongaling whose appointment in 1970 has proven the worth of the Bishop's appointment via improvements in physical plant and strong educational leadership. The academic administration was entrusted to Mrs. Flora V. Javier, High School Principal and Miss Leodegaria A. Ciceron, Grade School Principal.
                          </p>
                          
                          <p>
                          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                          School year 1975 - 1976 was when the night session opened in answer to the demands of the community to provide Catholic education to those who had to earn a living during the day.  The night classes lasted for 21 years.
                          </p>
                  </div>
                  <div class="col-md-4"><img alt="" src="img/stc_logo.png" class="img-fluid rounded-circle" style="margin-top: 36px;" /></div>
                  
                  <div class="col-md-12">
                          <p>
                          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                          In 1984, Rev. Fr. Conrado G. Castillo took over the directorial job of the institution. Under the new administration, the main thrusts were the upliftment of the employees' status, articulation of the value system and Teresian spirituality in the school's curriculum and the improvement of the school services and facilities. Those became the core thrusts of his administration until the expiration of his term.
                          </p>
                          
                          <p>
                          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                          Fr. Castillo implemented his development plans amidst difficulties. Several structures were living proofs of his dedicated directorship, such as the renovation of the classrooms, the construction of the Chapel, the canteen, the bookstore, Home Economics laboratories, the school clinic, computer and speech laboratories, covered walks, children's playground and waiting shed for parents complete with tables and benches. He also revived the Family Council and strengthened the STA Alumni Association.
                          </p>
                          
                          <p>
                          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                          In 1990 the school held a year-long celebration of its Golden Anniversary. The activities included symposia for parents, alumni and pupils, a dance party and a cultural presentation which coincided with the Coronation night for Mr. and Miss STC and Little Mr. and Miss STC for the high school and grade school departments respectively. 
                          </p>
                          
                          <p>
                          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                          In the last 20 years, STC has increased its enrolment by leaps and bounds from barely 400 pupils in 1969, its first year of independent operation to 2,379 students in 1990. In the course of time, STC became especially distinguished for its high standard of education. 
                          </p>
                  
                  </div>
                   
                  
                  <div class="col-md-8">
                  <p class="lead" style="margin-top: 36px; margin-bottom: 12px; ">The STC era</p>
                          <p>
                          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                          Based on the premise of the three core-values of CONCERN, RESPONSIBILITY and SERVICE of Teresian community, Fr. Castillo pursued to open a quality affordable tertiary school which was fnally realized through the opening of Sta. Teresa College and marked his installation as STC's first president.
                          </p>
                          
                          <p>
                          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                          With the blessings of the Archbishop, he spearheaded his biggest project so far - the establishment of a College Department for which he built the three-storey college building. Such endeavor commenced with a ground breaking ceremony on September 1994 and was officiated by the Most Rev. Gaudencio B. Rosales, Archbishop of Lipa; witnessed by the Administration, the Faculty and staff and the officers of the Family Council.
                          </p>
                  </div>
                  <div class="col-md-4"><img alt="" src="img/stc_logo.png" class="img-fluid rounded-circle" style="margin-top: 36px;" /></div>
                  
                  <div class="col-md-12">
                          <p>
                          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                          June of 1995 marked the maiden operation of the College Department for which St. Theresa's Academy was renamed Sta. Teresa College. The first college dean, Dr. Flora V. Javier, tapped some grade school teachers and part timers to form the academic staff. On October 15, the College Building was blessed and the first College President, Rev. Fr. Conrado G. Castillo, was installed.
                          </p>
                          
                          <p>
                          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                          Starting a college is an arduous task. STC has to compete with the more-established, if not well-established colleges and universities in the city; consequently, the record of college enrollment was not that big at the start with 58 students during the first semester.
                          </p>
                          
                          <p>
                          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                          In the course of time, STC withstood the test and challenges and has painstakingly maintained its high academic status that slowly attracted college enrollees from the nearby towns and far provinces as well.
                          </p>
                          
                          <p>
                          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                          The year 1997 marked the awarding of the first College diplomas to the 23 and 15 graduates of Associates in Computer Technology and Computer Secretarial respectively while the first Degree Holders - 17 in Bachelor of Science in Computer Science, 2 in Bachelor of Secondary Education and 9 in Bachelor of Elementary Education received their diplomas in 1999. 
                          </p>
                          
                          <p>
                          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                          In 1997, Miss Emelinda G. Castillo succeeded Dr. Flora V. Javier as dean. In 1999, Miss Leodegaria A. Ciceron was named Director for Academics. As in charge at the academics helm, she held the position of the College Dean and Principal of the Basic Education Department. Likewise, Mr. Aurelio D. Manalo Jr., was appointed Director for Administrative Services which include the Finance department. 
                          </p>
                          
                          <p>
                          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                          Coinciding with the start of the New Millennium, a Grand Alumni Homecoming in celebration of STC's 60th Anniversary was held. The Alumni Association led the affairs which included their participation in the mass in honor of the school Patroness, St. Teresa of Avila; parade of muses on the first day of intramurals; alumni night which was highlighted by the presentation of the Mr. & Ms. STC grade school, high school and college; Ms. Alumna of the Decades, and Mr. & Ms. Faculty; and Ballroom Dancing / party. In return, a cultural presentation was performed by the Cherubim, Siglayaw and Teatro Teresiana talents in salute to the alumni.
                          </p>
                  </div>
                   
                  <div class="col-md-8">
                  <p class="lead" style="margin-top: 36px; margin-bottom: 12px; ">At Present</p>
                          <p>
                          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                          The year 2004 highlighted major changes in the administration. On October 15, coinciding with the 64th foundation anniversary of the institution, Rev. Fr. Carlo Magno C. Ilagan was installed as second College President, replacing Fr. Castillo. The event was officiated by Most Rev. Ramon C. Arguelles, DD, STL, Archbishop of Lipa. Mrs. Aida A. Casas became the Dean of College and Miss Pelagia C. Manalo was appointed Principal in charge of the Basic Education.
                          </p>
                          
                          <p>
                          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                          Fr. Ilagan continued the development and improvement pioneered by Fr. Castillo, including the expansion of the College Building and the establishment of the centralized school library and multipurpose hall. He aimed for the intellectual formation and development of competitiveness among the pupils as he launched the campus wide English policy campaign. It was with Fr. Ilagan's initiative that Unified Schools of the Archdiocese of Lipa (USAL), umbrella organization of all the Archdiocesan Schools, was established where STC served as a front runner.
                          </p>
                  </div>
                  <div class="col-md-4"><img alt="" src="img/stc_logo.png" class="img-fluid rounded-circle" style="margin-top: 36px;" /></div>
                  
                  <div class="col-md-12">
                          <p>
                          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                          Development and renovations marked the progress of Sta. Teresa College. Among which were the construction of the fourth floor of the College Building which housed the Bulwagang Teresiana, an additional two-storey building in the grade school which has five classrooms, a science laboratory and multi-function hall, the extension and renovation of the canteen to accommodate more pupils, reconstruction of the shop, the newly-landscaped garden, and the physical development of the Prep Town, an environment built for the young learners to experience a different kind of learning atmosphere. Air conditioning units, TV sets and LCD's were installed in the classrooms and every corner served as an extension of the learning atmosphere.
                          </p>
                          
                          <p>
                          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                          School year 2006-2007 marked a change in the administration Mrs. Aida A. Casas was designated Director of Academics which she held for a year as she was replaced by Dr. Randy M. Baja who likewise became the Dean of College. Mrs. Lourdes L. Cadevida was appointed the Grade School Principal and and Ms. Pelagia C. Manalo, the Grade School Principal. Mr. Aurelio D. Manalo, Jr. was reappointed as Director of Finance and Administration.
                          </p>
                          
                          <p>
                          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                          STC adopted Missionary Catechist of the Sacred Heart (MCSH) School in Isla Verde as her mission school during thr school year 2008-2009 and the School Motto was born. "Always for Jesus" became a Teresian byword. The school year closed as STC put in the music map when STC Cherubim won the Championship in an international music competition in Malaysia.
                          </p>
                          
                          <p>
                          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                          On Top of the many activities for 2010 to mark STC's 70th year, the HS building which housed a century long history was restored to its former grandeur through the initiative of Fr. Ilagan. Its rural historic setting is a rare survival. On the academic front, meanwhile, the Grade School Department passed the accreditation of the PAASCU while the Grade School Department is an ESC-FAPE grantee. During this year, Dr. Randy M. Baja ran the Basic Education Department as School Principal.
                          </p>
                          
                          <p>
                          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                          More improvements were done since 2010. The widening of the canteen was taken into consideration to serve the continuously growing population. This year, an additional of eight classrooms were constructed on the second floor of the Grade School building adjacent to the college building to house more pupils. Additional rooms include a music room, simulation room, offices for the prefect of discipline and moderator of cultural affairs, and for the Human Resource Management and Development which is now in full blast operation. A covered gymnasium, now named San Lorenzo Ruiz Hall was also constructed as STC grounds to accommodate comfortably even the entire community for special gatherings.
                          </p>
                          
                          <p>
                          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                          Year 2015 marks another momentous event in the history of STC as it celebrated its 75th founding anniversary this October. A change in the administration was made. A new breed of administrators was appointed for school year 2015-2016 headed by the new school president, Rev. Fr. Ricardo A. Panganiban. Dr. Randy M. Baja was appointed Chief Operations Officer of the College Department while Mr. Aurelio Manalo Jr. was appointed Chief Operations Officer of the Basic Education Department and the Head of the accounting Office. Mrs. Violeta C. Ramos was reappointed Principal this time of the Basic Education Department assisted by Mrs. Mercy R. Villanueva for the Grade School Department and Ms. Rizza Castillo for the Grade School Department.
                          </p>
                          
                          <p>
                          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                          At present, the grade school department is on its level 2 and the High School department, level 1 in the PAASCU accreditation.
                          </p>
                          
                          <p>
                          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                          As STC passes another milestone in her colorful history, it is with a high note that she recognizes the roles and CONCERN everyone played for her development. But with a sense of reverence she also reminds the RESPONSIBILITY everybody must share to be of SERVICE to all those who need one.
                          </p>
                          
                          <p>
                          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                          Today, STA. TERESA COLLEGE stands as a symbol of commitment to the ideals of genuine and authentic Christian education, responding to the global milieu and the Holy Mother Church, and meeting the demands and challenges of the changing times.
                          </p>
