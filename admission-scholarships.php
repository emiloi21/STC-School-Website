                              <h5>EDUCATION SERVICE CONTRACTING (ESC)</h5>
                              </p>
                              
                              <p class="lead">
                              <strong>What is ESC?</strong><br />
                              ESC is a GASTPE program wherein financial assistance (in the form of tuition subsidies) is extended to qualified elementary school graduates who wish to pursue Junior High School (JHS) - Grades 7 to 10 - in private schools.
                              </p>
                              
                              <p class="lead">
                              <strong>How does ESC work?</strong><br />
                              JHSs apply and are certified by DepEd through PEAC to be part of the program.<br />
                              JHSs that become ESC-participating schools are given a number of slots that come with subsidies called ESC grants.<br />
                              Program beneficiaries are called ESC grantees.
                              </p>
                              
                              <p class="lead">
                              <strong>Who are ESC grantees?</strong><br />
                              Those who want to be ESC grantees must enroll for Grade 7 in an ESC-participating school. Each ESC-participating school has a School Committee that will select Grade 7 ESC grantees. No new ESC grants are awarded at higher grade levels.
                              </p>
                              
                              <p class="lead">
                              <strong>How are ESC grantees selected?</strong>
                              <ul class="lead">
                              <strong style="font-size: 14px;">Eligibility</strong>
                              <li>Elementary graduate from a DepEd public school or DepEd-recognized private school</li>
                              <li>Incoming Grade 7 student</li>
                              <li>Has not been a recipient of the ESC in previous school years</li>
                              <li>Filipino citizen</li>
                              </ul>
                              
                              <ul class="lead">
                              <strong style="font-size: 14px;">Requirements</strong>
                              <li>ESC Application Form</li>
                              <li>Recent Identical 2x2 ID photo (2 copies)</li>
                              <li>For leaners with no learner profile in the Learner Information System (LIS): Certified true copy of Philippine Statistics Authority (PSA)</li>
                              <li>Certified Birth Certificate</li>
                              <li>Grade 6 Report Card</li>
                              <li>Proof of income of parents/guardian</li>
                              </ul>
                              
                              <ul class="lead">
                              <strong style="font-size: 14px;">Evaluation</strong>
                              <li>Selection is subject to the limited slots allocated to the ESC-participating school</li>
                              <li>Elementary graduates of DepEd public schools are automatic ESC grantees</li>
                              <li>For elementary graduates of DepEd-recognized private schools, the School Committee shall profile and assess the students based on the documentary requirements submitted</li>
                              </ul>
                              
                              </p>
                              
